/*
📤 销售出库数据查询
用途：查看仓库发货给客户的记录
就像查看"今天仓库发出了哪些快递给买家"
*/

select 
    *,                                    -- 选择所有原始字段（把所有信息都显示出来）
    case when right(left(fserial_no,6),2)="16" then "杭州" else "深圳" end as fwarehouse,
    -- ↑ 判断货物是从哪个仓库发出的：
    -- 如果序列号中包含"16"，就是从杭州仓库发出
    -- 否则就是从深圳仓库发出
    -- 就像看快递单上的发货地址
    
    left(fserial_no,2) as fchannel       -- 从序列号前2位看出是哪个销售渠道
from dwd.dwd_t_pm_wms_stock_notify       -- 从"仓库库存通知表"查找数据
where to_date(fcreate_time)>='2024-01-01'  -- 只看2024年以后的记录
and fcmd='JYCK'                          -- 操作类型是"JYCK"（交易出库）

/*
💡 简单解释：
这个查询就像问仓库发货员：
"给我看看2024年以后所有发出去的货物记录，
告诉我每件货是从哪个仓库发出的，通过哪个渠道卖出的"

🔍 关键词解释：
- JYCK = 交易出库（Jiao Yi Chu Ku的拼音首字母）
- 就像淘宝商家把商品发给买家的过程
*/
